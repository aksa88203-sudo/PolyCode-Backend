package main

import "fmt"

func main() {
	// String variable
	name := "Go Programmer"
	fmt.Println("Hello,", name)
	
	// Integer variable
	age := 25
	fmt.Printf("You are %d years old\n", age)
	
	// Boolean variable
	isLearning := true
	fmt.Printf("Are you learning Go? %t\n", isLearning)
	
	// Float variable
	score := 95.5
	fmt.Printf("Your score: %.1f\n", score)
}
