// =========================================
// COMMAND LINE ARGUMENT PARSING
// =========================================

fn parse_arguments() -> (bool, bool, bool, bool) {
    let args: Vec<String> = std::env::args().collect();
    
    let mut demo_basic = false;
    let mut demo_features = false;
    let mut demo_platform = false;
    let mut demo_environment = false;
    
    for arg in args.iter().skip(1) {
        match arg.as_str() {
            "--basic" => demo_basic = true,
            "--features" => demo_features = true,
            "--platform" => demo_platform = true,
            "--environment" => demo_environment = true,
            "--all" => {
                demo_basic = true;
                demo_features = true;
                demo_platform = true;
                demo_environment = true;
            }
            "--interactive" => {
                #[cfg(feature = "binary-features")]
                {
                    binary_features::interactive_mode();
                    std::process::exit(0);
                }
                #[cfg(not(feature = "binary-features"))]
                {
                    println!("Interactive mode requires 'binary-features' feature");
                    std::process::exit(1);
                }
            }
            "--help" => {
                print_help();
                std::process::exit(0);
            }
            _ => {
                println!("Unknown argument: {}", arg);
                print_help();
                std::process::exit(1);
            }
        }
    }
    
    // If no arguments provided, show all demos
    if !demo_basic && !demo_features && !demo_platform && !demo_environment {
        demo_basic = true;
        demo_features = true;
        demo_platform = true;
        demo_environment = true;
    }
    
    (demo_basic, demo_features, demo_platform, demo_environment)
}

fn print_help() {
    println!("Cargo Build System Demonstration");
    println!("Usage: cargo run --bin build_system_and_cargo [OPTIONS]");
    println!();
    println!("Options:");
    println!("  --basic       Show basic functionality demo");
    println!("  --features    Show feature flags demo");
    println!("  --platform    Show platform-specific demo");
    println!("  --environment Show environment variables demo");
    println!("  --all         Show all demos (default)");
    println!("  --interactive Run in interactive mode");
    println!("  --help        Show this help message");
    println!();
    println!("Feature flags to try:");
    println!("  cargo run --features 'serde-support'");
    println!("  cargo run --features 'async'");
    println!("  cargo run --features 'binary-features'");
    println!("  cargo run --features 'build-script'");
    println!();
    println!("Build profiles:");
    println!("  cargo run --release");
    println!("  cargo run --profile custom");
}

// =========================================
// MAIN FUNCTION
// =========================================

fn main() {
    println!("=== CARGO BUILD SYSTEM DEMONSTRATION ===");
    println!("Run with --help for usage information");
    println!();
    
    let (demo_basic, demo_features, demo_platform, demo_environment) = parse_arguments();
    
    if demo_basic {
        demonstrate_basic_features();
    }
    
    if demo_features {
        demonstrate_features();
    }
    
    if demo_platform {
        demonstrate_platform_specific();
    }
    
    if demo_environment {
        demonstrate_environment();
    }
    
    demonstrate_build_info();
    
    #[cfg(feature = "binary-features")]
    if demo_basic {
        demonstrate_binary_features();
    }
    
    println!("\n=== DEMONSTRATION COMPLETE ===");
    println!("Try running with different options:");
    println!("  cargo run --bin build_system_and_cargo --features 'serde-support'");
    println!("  cargo run --bin build_system_and_cargo --release");
    println!("  cargo run --bin build_system_and_cargo --target x86_64-unknown-linux-musl");
}

