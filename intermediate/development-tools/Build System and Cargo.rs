#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_calculate_fibonacci() {
        assert_eq!(calculate_fibonacci(0), 0);
        assert_eq!(calculate_fibonacci(1), 1);
        assert_eq!(calculate_fibonacci(5), 5);
        assert_eq!(calculate_fibonacci(10), 55);
    }

    #[test]
    fn test_process_data() {
        let input = vec![1, 2, 3, 4, 5];
        let expected = vec![2, 3, 8, 8, 6];
        assert_eq!(process_data(&input), expected);
    }

    #[test]
    fn test_format_message() {
        let result = format_message("Alice", 42);
        assert_eq!(result, "Hello Alice! You have 42 items.");
    }

    #[test]
    fn test_serializable_data() {
        let data = SerializableData {
            name: "Test".to_string(),
            value: 42,
        };
        
        assert_eq!(data.name, "Test");
        assert_eq!(data.value, 42);
    }

    #[test]
    fn test_environment_variables() {
        // These should be available when running tests
        let profile = std::env::var("PROFILE").unwrap_or_default();
        let target = std::env::var("TARGET").unwrap_or_default();
        
        assert!(!profile.is_empty());
        assert!(!target.is_empty());
    }

    #[cfg(feature = "binary-features")]
    #[test]
    fn test_enhanced_processing() {
        let input = vec![1, 2, 3];
        let (processed, count, sum) = binary_features::enhanced_processing(&input);
        
        assert_eq!(processed, vec![2, 3, 8]);
        assert_eq!(count, 3);
        assert_eq!(sum, 13);
    }
}

