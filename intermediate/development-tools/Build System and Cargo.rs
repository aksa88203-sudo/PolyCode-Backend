#[cfg(feature = "binary-features")]
mod binary_features {
    use super::*;
    
    pub fn enhanced_processing(data: &[i32]) -> (Vec<i32>, usize, i32) {
        let processed = process_data(data);
        let sum: i32 = processed.iter().sum();
        let count = processed.len();
        
        (processed, count, sum)
    }
    
    pub fn interactive_mode() {
        println!("=== INTERACTIVE MODE ===");
        
        loop {
            println!("Enter a number (or 'quit' to exit):");
            let mut input = String::new();
            std::io::stdin().read_line(&mut input).unwrap();
            
            let input = input.trim();
            
            if input == "quit" {
                break;
            }
            
            match input.parse::<i32>() {
                Ok(num) => {
                    let result = calculate_fibonacci(num as u32);
                    println!("fibonacci({}) = {}", num, result);
                }
                Err(_) => {
                    println!("Invalid number. Please try again.");
                }
            }
        }
        
        println!("Goodbye!");
    }
}

