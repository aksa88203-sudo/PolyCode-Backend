pub fn calculate_fibonacci(n: u32) -> u64 {
    match n {
        0 => 0,
        1 => 1,
        n => calculate_fibonacci(n - 1) + calculate_fibonacci(n - 2),
    }
}

pub fn process_data(data: &[i32]) -> Vec<i32> {
    data.iter()
        .map(|x| if *x % 2 == 0 { x * 2 } else { x + 1 })
        .collect()
}

pub fn format_message(name: &str, count: usize) -> String {
    format!("Hello {}! You have {} items.", name, count)
}

